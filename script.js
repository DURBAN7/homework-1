function calc(num1, num2, math) {
  if (math === '+') {
    console.log(num1 + num2);
  } else if (math === '-') {
    console.log(num1 - num2);
  } else if (math === '*') {
    console.log(num1 * num2);
  } else if (math === '/') {
    console.log(num1 / num2);
  } else {
    console.log('Ошибка');
  }
}

calc(18, 17, '*');
